void main()
{
	 unsigned char i = 0;
	 unsigned char Offset = 0;
	 unsigned char Count = 0;
	 MatrixLED_Init();
   while(1)
   {
		 for(i = 0; i < 8; ++i)
		 {
				MatrixLED_showColumn(i,Animations[i+Offset]);
		 }
		 Count++;
		 if(Count > 15)
		 {
				Count = 0;
			  Offset += 8;
				if(Offset>16)
					Offset = 0;
		 }
   }
}